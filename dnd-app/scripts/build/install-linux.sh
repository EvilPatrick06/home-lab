#!/usr/bin/env bash
# D&D VTT — Linux installer
#
# One-line install (auto-applies PATH to your current shell):
#   curl -fsSL https://github.com/EvilPatrick06/home-lab/releases/latest/download/install-linux.sh | bash && source ~/.bashrc
#
# The `&& source ~/.bashrc` part runs in your *current* shell after the
# install script finishes, so the new PATH entry takes effect immediately
# — `dnd-vtt` works as a command without restarting your terminal. This
# script (running in a `bash` subprocess) can't reach into your shell's
# environment on its own; that's a fundamental Unix subprocess boundary.
#
# What this script does:
#   1. Resolves the latest AppImage URL from the GitHub release
#   2. Downloads to ~/Applications/dnd-vtt.AppImage and chmod +x
#   3. Drops a launcher at ~/.local/bin/dnd-vtt (auto-detects VM/Wayland/GPU)
#   4. Drops a .desktop entry in ~/.local/share/applications/
#   5. Extracts the app icon into ~/.local/share/icons/hicolor/.../apps/
#   6. Auto-runs `sudo apt install -y` for any missing runtime libs
#   7. Appends ~/.local/bin to PATH via ~/.bashrc / ~/.profile
#
# Won't auto-install Ollama. AI features need Ollama
# (`curl -fsSL https://ollama.com/install.sh | sh`) or cloud API keys
# set in-app.

set -euo pipefail

REPO="EvilPatrick06/home-lab"
APP_NAME="dnd-vtt"
DISPLAY_NAME="D&D Virtual Tabletop"
INSTALL_DIR="${HOME}/Applications"
DESKTOP_DIR="${HOME}/.local/share/applications"
ICON_DIR="${HOME}/.local/share/icons/hicolor/512x512/apps"
BIN_DIR="${HOME}/.local/bin"
DEST="${INSTALL_DIR}/${APP_NAME}.AppImage"
LAUNCHER="${BIN_DIR}/${APP_NAME}"

bold() { printf "\033[1m%s\033[0m\n" "$*"; }
info() { printf "  %s\n" "$*"; }
ok()   { printf "\033[32m✓\033[0m %s\n" "$*"; }
err()  { printf "\033[31m✗\033[0m %s\n" "$*" >&2; }

bold "==> Installing ${DISPLAY_NAME}"

# Resolve latest AppImage from GitHub Releases API
info "Querying latest release..."
APPIMAGE_URL=$(
  curl -fsSL "https://api.github.com/repos/${REPO}/releases/latest" \
    | grep -oE 'https://[^"]+x86_64\.AppImage' \
    | head -1
)

if [[ -z "${APPIMAGE_URL}" ]]; then
  err "Could not find an x86_64 AppImage asset on the latest release."
  err "Browse releases at https://github.com/${REPO}/releases and download manually."
  exit 1
fi

VERSION=$(echo "${APPIMAGE_URL}" | grep -oE '[0-9]+\.[0-9]+\.[0-9]+' | head -1)
info "Version: ${VERSION:-unknown}"

mkdir -p "${INSTALL_DIR}"

bold "==> Downloading"
info "${APPIMAGE_URL}"
info "to ${DEST}"
# Download to a tmp path and atomic-rename so a Ctrl+C mid-download
# doesn't leave a half-finished file with a misleading name.
curl -fL --progress-bar "${APPIMAGE_URL}" -o "${DEST}.tmp"
mv -f "${DEST}.tmp" "${DEST}"
chmod +x "${DEST}"
ok "Downloaded + made executable"

bold "==> Adding launcher script"
mkdir -p "${BIN_DIR}"
# Wrapper that auto-detects GPU + sandbox availability and tacks on the
# right flags. Putting the logic at launch time (not install time) means a
# user who installs in a VM and later runs the AppImage on bare metal —
# or vice versa — doesn't end up with stale flags.
#
# Why each flag:
#   --no-sandbox        chrome-sandbox needs setuid root; AppImages can't
#                       ship a setuid binary, and Ubuntu 24.04+ also blocks
#                       unprivileged userns via AppArmor.
#   --disable-gpu       drmGetDevices2 returns nothing in headless / VM /
#                       no-DRI environments; without this Electron hangs
#                       on GPU init and never paints a window.
#   --ozone-platform=x11  forces X11 even when the session advertises
#                       Wayland; some Wayland compositors crash the
#                       Electron renderer on the third-party GL stack.
cat > "${LAUNCHER}" <<'LAUNCHER_EOF'
#!/usr/bin/env bash
# Auto-generated by dnd-vtt install-linux.sh — safe to edit if you know
# what you're doing.
set -e

APPIMAGE="__APPIMAGE_PATH__"
FLAGS=(--no-sandbox)

# No DRI render nodes → no GPU. Force CPU rendering so the renderer
# doesn't hang on GPU initialization.
if ! ls /dev/dri/renderD* >/dev/null 2>&1; then
  FLAGS+=(--disable-gpu)
fi

# If the session is Wayland but we don't have a working ozone-wayland
# build, prefer X11. This is harmless on pure-X11 sessions.
if [[ "${XDG_SESSION_TYPE:-}" == "wayland" ]]; then
  FLAGS+=(--ozone-platform=x11)
fi

# First-run AppImage extraction can take 10-30s with no terminal output.
# Without this hint, users assume the launch hung and Ctrl+C. Only
# print when stdout is a tty (not when launched from the app menu).
if [[ -t 1 ]]; then
  echo "Starting D&D Virtual Tabletop..."
  echo "(first launch can take 10-30s — extracting AppImage to /tmp)"
fi

exec "${APPIMAGE}" "${FLAGS[@]}" "$@"
LAUNCHER_EOF
sed -i "s|__APPIMAGE_PATH__|${DEST}|" "${LAUNCHER}"
chmod +x "${LAUNCHER}"
ok "Added ${LAUNCHER} (wraps the AppImage with auto-detected flags)"

# Make sure ~/.local/bin is on PATH so the "run from terminal" hint at
# the end actually works. We auto-append to ~/.bashrc (and ~/.profile
# for login shells / non-bash users like fish, zsh-with-bashrc-source,
# Debian's default `dash` /bin/sh as login shell). Idempotent: we look
# for our marker line before adding it, so re-running install-linux.sh
# doesn't pile up duplicate exports.
if ! echo "${PATH}" | tr ':' '\n' | grep -qx "${BIN_DIR}"; then
  added_to=""
  path_marker='# dnd-vtt installer: ensure ~/.local/bin is on PATH'
  path_export='export PATH="$HOME/.local/bin:$PATH"'
  for rc in "${HOME}/.bashrc" "${HOME}/.profile"; do
    if [[ -f "${rc}" ]] && ! grep -Fq "${path_marker}" "${rc}"; then
      printf '\n%s\n%s\n' "${path_marker}" "${path_export}" >> "${rc}"
      added_to="${added_to}${rc} "
    fi
  done
  echo
  if [[ -n "${added_to}" ]]; then
    ok "Added ${BIN_DIR} to PATH in: ${added_to}"
    info "To make \`${APP_NAME}\` work in your CURRENT terminal, run this once:"
    info ""
    info "    source ~/.bashrc"
    info ""
    info "(Or use the full path: ${LAUNCHER}.) Next time, you can chain the"
    info "source step into the install command itself:"
    info ""
    info "    curl -fsSL .../install-linux.sh | bash && source ~/.bashrc"
  else
    info "Note: ${BIN_DIR} isn't on your PATH yet."
    info "Couldn't auto-append to ~/.bashrc or ~/.profile (do they exist?)."
    info "Run this manually:    echo '${path_export}' >> ~/.bashrc"
    info "Or use the full path: ${LAUNCHER}"
  fi
fi

bold "==> Extracting app icon"
# AppImages ship a top-level <name>.png that the .desktop file's Icon=
# field references. Without copying it into the user's hicolor theme
# tree, the app menu shows a generic placeholder icon instead of the
# BMO/D&D artwork. Extract into a tmp dir, copy the icon out, clean up.
mkdir -p "${ICON_DIR}"
icon_tmp=$(mktemp -d)
icon_extracted=0
if (cd "${icon_tmp}" && "${DEST}" --appimage-extract "${APP_NAME}.png" >/dev/null 2>&1); then
  icon_src="${icon_tmp}/squashfs-root/${APP_NAME}.png"
  if [[ -f "${icon_src}" ]]; then
    cp "${icon_src}" "${ICON_DIR}/${APP_NAME}.png"
    icon_extracted=1
  fi
fi
# Fallback: search for any PNG inside the extracted resources tree.
if [[ "${icon_extracted}" -eq 0 ]]; then
  if (cd "${icon_tmp}" && "${DEST}" --appimage-extract "usr/share/icons/hicolor/*/apps/${APP_NAME}.png" >/dev/null 2>&1); then
    icon_src=$(find "${icon_tmp}/squashfs-root" -name "${APP_NAME}.png" 2>/dev/null | sort -r | head -1)
    if [[ -n "${icon_src}" && -f "${icon_src}" ]]; then
      cp "${icon_src}" "${ICON_DIR}/${APP_NAME}.png"
      icon_extracted=1
    fi
  fi
fi
rm -rf "${icon_tmp}"
if [[ "${icon_extracted}" -eq 1 ]]; then
  gtk-update-icon-cache -t "${HOME}/.local/share/icons/hicolor" 2>/dev/null || true
  ok "Installed icon to ${ICON_DIR}/${APP_NAME}.png"
else
  err "Could not extract icon from AppImage — app menu will show a generic placeholder."
  err "(This is cosmetic; the app still launches normally.)"
fi

bold "==> Adding desktop entry"
mkdir -p "${DESKTOP_DIR}"
cat > "${DESKTOP_DIR}/${APP_NAME}.desktop" <<EOF
[Desktop Entry]
Type=Application
Name=${DISPLAY_NAME}
Comment=D&D Virtual Tabletop — Electron desktop app for D&D 5e
Exec=${LAUNCHER}
Icon=${APP_NAME}
Categories=Game;RolePlaying;
Terminal=false
StartupWMClass=${APP_NAME}
EOF
update-desktop-database "${DESKTOP_DIR}" 2>/dev/null || true
ok "Added ${DESKTOP_DIR}/${APP_NAME}.desktop"

# Sanity-check common runtime deps. AppImages need libfuse2 on Ubuntu/Debian;
# Electron needs the GTK/Nss/Xss/Alsa stack. We don't auto-apt-install (this
# script intentionally avoids sudo), but we surface the gap with a copy-
# paste-able install line so the user isn't left guessing.
missing_libs=()
need_lib() {
  if ! ldconfig -p | grep -q "$1"; then
    missing_libs+=("$2")
  fi
}
need_lib "libfuse.so.2" "libfuse2"
need_lib "libnss3.so"   "libnss3"
need_lib "libgbm.so.1"  "libgbm1"
need_lib "libasound.so.2" "libasound2"
need_lib "libgtk-3.so.0" "libgtk-3-0"

if [[ ${#missing_libs[@]} -gt 0 ]]; then
  echo
  bold "==> Installing required runtime libraries"
  info "Missing: ${missing_libs[*]}"
  info "Electron will not launch without these — installing automatically."
  echo
  if command -v apt-get >/dev/null 2>&1; then
    # apt-get reads its yes/no prompts from stdin, but we add -y to skip.
    # sudo reads the password from /dev/tty directly (not stdin), so this
    # works even when the script is piped via `curl | bash` — the password
    # prompt still appears on the user's terminal.
    info "Running: sudo apt install -y ${missing_libs[*]}"
    info "(your terminal may prompt for your password)"
    echo
    if sudo apt-get install -y "${missing_libs[@]}"; then
      ok "Installed: ${missing_libs[*]}"
    else
      err "Auto-install failed (apt returned non-zero)."
      err "Run manually: sudo apt install ${missing_libs[*]}"
      err "Then re-run the AppImage — no need to re-run install-linux.sh."
    fi
  else
    err "Auto-install only supports Debian/Ubuntu (apt-get not found)."
    err "Install these libraries manually for your distro: ${missing_libs[*]}"
    err "Common equivalents:"
    err "  Fedora:  sudo dnf install nss libgtk-3 mesa-libgbm alsa-lib fuse"
    err "  Arch:    sudo pacman -S nss gtk3 mesa alsa-lib fuse2"
  fi
fi

bold "==> Done"
info "Run from terminal:  ${APP_NAME}    (or full path: ${LAUNCHER})"
info "Or launch from your app menu: ${DISPLAY_NAME}"
echo
info "Local AI (optional, for Ollama):"
info "    curl -fsSL https://ollama.com/install.sh | sh"
info "    ollama pull llama3.2"
info "Cloud AI: set API keys in app → Settings → AI Providers"
